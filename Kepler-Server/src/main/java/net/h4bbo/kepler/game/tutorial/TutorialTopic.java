package net.h4bbo.kepler.game.tutorial;

public class TutorialTopic {
    private int id;
    private String name;
    private int status;

    public TutorialTopic() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
